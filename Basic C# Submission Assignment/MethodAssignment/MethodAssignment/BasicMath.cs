namespace MethodAssignment
{
    public class BasicMath
    {
        public int addNumber(int userNum)
        {
            int result = userNum + 10;
            return result;
        }

        public int subtractNumber(int userNum)
        {
            int result = userNum - 10;
            return result;
        }

        public int multiplyNumber(int userNum)
        {
            int result = userNum * 10;
            return result;
        }
    }
}
